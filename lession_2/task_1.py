# 1. Выяснить тип результата выражений:
# 15 * 3
# 15 / 3
# 15 // 2
# 15 ** 2

print(f'тип результата выражения 15 * 3 = {type(15 * 3)}')
print(f'тип результата выражения 15 / 3 = {type(15 / 3)}')
print(f'тип результата выражения 15 // 2 = {type(15 // 3)}')
print(f'тип результата выражения 15 ** 2 = {type(15 ** 3)}')